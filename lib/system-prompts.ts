export const TOKEN_PRICING: Record<string, { inputPerMtok: number; outputPerMtok: number }> = {
  "claude-haiku-4-5-20251001": { inputPerMtok: 1_00, outputPerMtok: 5_00 },
  "claude-sonnet-4-6": { inputPerMtok: 3_00, outputPerMtok: 15_00 },
  "claude-opus-4-7": { inputPerMtok: 15_00, outputPerMtok: 75_00 },
}

export function calculateCostCents(model: string, inputTokens: number, outputTokens: number): number {
  const pricing = TOKEN_PRICING[model]
  if (!pricing) return 0
  const inputCost = (inputTokens / 1_000_000) * pricing.inputPerMtok
  const outputCost = (outputTokens / 1_000_000) * pricing.outputPerMtok
  return Math.ceil(inputCost + outputCost)
}
